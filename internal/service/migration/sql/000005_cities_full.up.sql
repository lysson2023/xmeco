-- 城市表：含 admin_code（行政区划代码）
CREATE TABLE city (
    id          SERIAL PRIMARY KEY,
    name        VARCHAR(100) NOT NULL,
    province    VARCHAR(50)  NOT NULL,
    location_id VARCHAR(20),           -- 和风天气 LocationID（预留）
    admin_code  VARCHAR(10),           -- 行政区划代码
    lat         DECIMAL(8,4),
    lon         DECIMAL(8,4),
    sort_order  INT DEFAULT 0
);

-- 天气缓存表重建
CREATE TABLE weather_cache (
    id          SERIAL PRIMARY KEY,
    city_id     INT NOT NULL REFERENCES city(id),
    location_id VARCHAR(100) NOT NULL,
    city_name   VARCHAR(100),
    temp        VARCHAR(10),
    feels_like  VARCHAR(10),
    icon        VARCHAR(10),
    weather_text VARCHAR(50),
    wind_dir    VARCHAR(20),
    wind_scale  VARCHAR(10),
    humidity    VARCHAR(10),
    precip      VARCHAR(10),
    pressure    VARCHAR(10),
    raw_json    TEXT,
    fetched_at  TIMESTAMPTZ DEFAULT NOW(),
    expires_at  TIMESTAMPTZ NOT NULL
);
CREATE INDEX IF NOT EXISTS idx_weather_cache_city ON weather_cache(city_id);
CREATE INDEX IF NOT EXISTS idx_weather_cache_location ON weather_cache(location_id);

