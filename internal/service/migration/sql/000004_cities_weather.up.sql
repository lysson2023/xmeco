-- 天气缓存表
CREATE TABLE IF NOT EXISTS weather_cache (
    id          SERIAL PRIMARY KEY,
    city_id     INT NOT NULL REFERENCES city(id),
    location_id VARCHAR(20) NOT NULL,
    temp        VARCHAR(10),          -- 当前温度
    feels_like  VARCHAR(10),          -- 体感温度
    icon        VARCHAR(10),          -- 天气图标代码
    weather_text VARCHAR(50),         -- 天气描述（晴/多云/雨等）
    wind_dir    VARCHAR(20),          -- 风向
    wind_scale  VARCHAR(10),          -- 风力等级
    humidity    VARCHAR(10),          -- 湿度
    precip      VARCHAR(10),          -- 降水量
    pressure    VARCHAR(10),          -- 气压
    raw_json    TEXT,                 -- 原始响应 JSON
    fetched_at  TIMESTAMPTZ DEFAULT NOW(),
    expires_at  TIMESTAMPTZ NOT NULL  -- 过期时间
);

CREATE INDEX IF NOT EXISTS idx_weather_cache_city ON weather_cache(city_id);
CREATE INDEX IF NOT EXISTS idx_weather_cache_location ON weather_cache(location_id);
